package com.xr.bos.pojo;


public class BasSubstitute {

  private Integer id;
  private String empNo;
  private String empName;
  private Integer mobileNo;
  private Integer type;
  private String pda;
  private double standardKg;
  private double models;
  private String plateInt;
  private String invalidateMark;
  private Integer subordinateUnit;
  private String stats;


  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }


  public String getEmpNo() {
    return empNo;
  }

  public void setEmpNo(String empNo) {
    this.empNo = empNo;
  }


  public String getEmpName() {
    return empName;
  }

  public void setEmpName(String empName) {
    this.empName = empName;
  }


  public Integer getMobileNo() {
    return mobileNo;
  }

  public void setMobileNo(Integer mobileNo) {
    this.mobileNo = mobileNo;
  }


  public Integer getType() {
    return type;
  }

  public void setType(Integer type) {
    this.type = type;
  }


  public String getPda() {
    return pda;
  }

  public void setPda(String pda) {
    this.pda = pda;
  }


  public double getStandardKg() {
    return standardKg;
  }

  public void setStandardKg(double standardKg) {
    this.standardKg = standardKg;
  }


  public double getModels() {
    return models;
  }

  public void setModels(double models) {
    this.models = models;
  }


  public String getPlateInt() {
    return plateInt;
  }

  public void setPlateInt(String plateInt) {
    this.plateInt = plateInt;
  }


  public String getInvalidateMark() {
    return invalidateMark;
  }

  public void setInvalidateMark(String invalidateMark) {
    this.invalidateMark = invalidateMark;
  }


  public Integer getSubordinateUnit() {
    return subordinateUnit;
  }

  public void setSubordinateUnit(Integer subordinateUnit) {
    this.subordinateUnit = subordinateUnit;
  }


  public String getStats() {
    return stats;
  }

  public void setStats(String stats) {
    this.stats = stats;
  }

}
