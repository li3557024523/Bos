package com.xr.bos.pojo;


public class BasArea {

  private Integer id;
  private String province;
  private String city;
  private String county;
  private Integer postalCode;
  private String simpleCode;
  private Integer cityCode;
  private Integer entryUnitId;
  private Integer complementUnitId;
  private String nature;
  private String theArea;
  private String stats;


  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }


  public String getProvince() {
    return province;
  }

  public void setProvince(String province) {
    this.province = province;
  }


  public String getCity() {
    return city;
  }

  public void setCity(String city) {
    this.city = city;
  }


  public String getCounty() {
    return county;
  }

  public void setCounty(String county) {
    this.county = county;
  }


  public Integer getPostalCode() {
    return postalCode;
  }

  public void setPostalCode(Integer postalCode) {
    this.postalCode = postalCode;
  }


  public String getSimpleCode() {
    return simpleCode;
  }

  public void setSimpleCode(String simpleCode) {
    this.simpleCode = simpleCode;
  }


  public Integer getCityCode() {
    return cityCode;
  }

  public void setCityCode(Integer cityCode) {
    this.cityCode = cityCode;
  }


  public Integer getEntryUnitId() {
    return entryUnitId;
  }

  public void setEntryUnitId(Integer entryUnitId) {
    this.entryUnitId = entryUnitId;
  }


  public Integer getComplementUnitId() {
    return complementUnitId;
  }

  public void setComplementUnitId(Integer complementUnitId) {
    this.complementUnitId = complementUnitId;
  }


  public String getNature() {
    return nature;
  }

  public void setNature(String nature) {
    this.nature = nature;
  }


  public String getTheArea() {
    return theArea;
  }

  public void setTheArea(String theArea) {
    this.theArea = theArea;
  }


  public String getStats() {
    return stats;
  }

  public void setStats(String stats) {
    this.stats = stats;
  }

}
